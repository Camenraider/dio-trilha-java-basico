public class OperadoresIncremento {
    public static void main(String[] args) {
        int numero = 5;

        // primeiro imprime o numero em cash e depois adiciona +1
        System.out.println(numero++);

        System.out.println(numero);

        // primeiro imprime o numero em cash e depois subtrai -1
        System.out.println(numero--);

        System.out.println(numero);
    }
}
